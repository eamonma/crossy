// The invite QR, vector-pinned (the vectors-before-implementations rule): every
// expected matrix below is the verbatim output of uqr (the generator the web
// already ships (apps/web/src/ui/PartyView.tsx renders the projector's QR with
// it), for the same payload at the same register (ECC M, no boost, automask).
// So the code an iPhone shows is module-for-module the code the projector
// shows, one invite QR across clients; a divergence is a bug in InviteQR,
// never a reason to fork the modules. Vectors were generated with
// uqr@0.1.3's encode(text, { ecc: "M", border: 0 }).

import XCTest

@testable import CrossyUI

