    // The degenerate empty payload still encodes (uqr's behavior): an empty
    // version-1 symbol, never a crash. No invite ever shares one; pinned so
    // the edge is deliberate.
    func test_emptyPayloadStillEncodes() throws {
        let matrix = try XCTUnwrap(InviteQR.matrix(for: ""))
