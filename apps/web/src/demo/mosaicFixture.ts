/** The five solvers, in slot order. Colors are the app's roster hues (fakeSession's SELF_COLOR
 * plus TEAMMATE_COLORS), the same set the presence chrome wears. */
export interface FixtureSolver {
  readonly id: string;
  readonly name: string;
  readonly color: string;
  readonly squares: number;
}

export const SOLVERS: readonly FixtureSolver[] = [
  { id: "u-mara", name: "Mara", color: "#3e63dd", squares: 55 },
  { id: "u-ivo", name: "Ivo", color: "#e5484d", squares: 33 },
  { id: "u-sena", name: "Sena", color: "#12a594", squares: 35 },
  { id: "u-dario", name: "Dario", color: "#ffb224", squares: 30 },
  { id: "u-lux", name: "Lux", color: "#8e4ec6", squares: 33 },
];

const BLOCK_SET = new Set(BLOCKS);

/** The fixture's Puzzle (geometry plus derived numbering). No circles, no wrong cells. */
export const MOSAIC_PUZZLE: Puzzle = (() => {
  const { numbers, acrossClues, downClues } = computeLayout(
    SIZE,
    SIZE,
    BLOCK_SET,
  );
  return {
    cols: SIZE,
    rows: SIZE,
    blocks: BLOCK_SET,
    numbers,
    circles: new Set<number>(),
    wrong: new Set<number>(),
    acrossClues,
    downClues,
  };
})();

/** The solved letters as the store's rendered-value map (cell index to glyph). */
export const MOSAIC_LETTERS: ReadonlyMap<number, string> = (() => {
  const map = new Map<number, string>();
  LETTER_ROWS.forEach((ch, index) => {
    if (ch !== null) map.set(index, ch);
  });
  return map;
})();

/** The abstract owner map: cell index to solver id, resolved from the fixture's slot attribution.
 * This is the shape a live caller derives from the store's last-writer `by`. */
export const MOSAIC_OWNER_MAP: OwnerMap = (() => {
  const map: Record<number, string> = {};
  for (const [cell, slot] of Object.entries(ATTRIBUTION)) {
    const solver = SOLVERS[slot];
    if (solver !== undefined) map[Number(cell)] = solver.id;
  }
  return map;
})();

/** The roster the owner ids resolve through (id to color). */
export const MOSAIC_ROSTER: Roster = (() => {
  const roster: Record<string, { color: string }> = {};
  for (const s of SOLVERS) roster[s.id] = { color: s.color };
  return roster;
})();
